/* WAP to take two numbers and print their sum, difference, prouct and division */

#include<iostream>
using namespace std;
int main()
{
    int a,b;
    cout<<"Enter the value of a :";
    cin>>a;
    cout<<"Enter the value of b :";
    cin>>b;
    cout<<"Sum :" <<a+b<<endl;
    cout<<"Difference :"<<a-b<<endl;
    cout<<"Product :"<<a*b<<endl;
    cout<<"Division :"<<a/b<<endl;
    return(0);
}